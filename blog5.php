  <p>El Día del Padre se celebra el 19 de marzo en los países de tradición católica europea por coincidir con el Día de San José, el padre de Jesús.</p>
  <p>Varios países europeos y la mayoría de países iberoamericanos adoptaron la fecha estadounidense, festejando el Día del Padre el tercer domingo de junio. En Rusia, coincide con el Día de los Defensores de la Patria, mientras que en Libia, Jordania y Palestina coincide con el primer día del verano, el 21 de junio.</p>

  <h2>Origen del Día del Padre</h2>
  <p>La idea de crear el Día del Padre surgió en Estados Unidos en 1910, cuando Sonora Smart Dodd quiso homenajear a su padre que la había criado en solitario. La fecha fue oficialmente establecida en 1966 por el presidente Lyndon Johnson para el tercer domingo de junio.</p>

  <h2>Celebración en España</h2>
  <p>En España, es tradición reunirse en familia, dar regalos y hacer manualidades en los colegios. La fecha es una oportunidad para expresar amor y gratitud hacia los padres, abuelos y bisabuelos.</p>

  <h2>¿Qué hacer para celebrar el Día del Padre?</h2>
  <p>Cualquier acto que demuestre tu aprecio será suficiente. Una llamada si estás lejos o una visita si estás cerca. Regalos como fotos enmarcadas, imanes de foto, libros o actividades compartidas son opciones.</p>
