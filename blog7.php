           <center>  <h1 class="pb-5">La importancia de hacer testamento</h1></center>
      
  <p>El testamento es un documento crucial por el cual el testador determina el destino de sus bienes, derechos y obligaciones después de su muerte. Garantiza que se cumplan sus decisiones sobre el futuro patrimonial y más.</p>

  <h2>Fallecer sin testamento</h2>
  <p>Fallecer sin expresar por escrito la voluntad sobre los bienes puede complicar la vida de los herederos. Facilita el camino en los trámites de la herencia y puede reducir costes económicos.</p>

  <h2>Tipos de testamentos</h2>
  <ul>
    <li>Testamento ante notario: más seguro y frecuente.</li>
    <li>Testamento escrito a mano (ológrafo): debe cumplir requisitos específicos y protocolizarse ante notario.</li>
  </ul>

  <h2>Recomendaciones</h2>
  <ul>
    <li>Mantener el cumplimiento del tratamiento.</li>
    <li>Mantener contacto con familiares y amigos.</li>
    <li>Contactar con recursos sanitarios y sociales ante cualquier situación de agravamiento.</li>
  </ul>

  <blockquote>
    <p>"Hacer testamento es un acto personal, revocable y libre, se puede modificar cuantas veces se quiera. Si a lo largo de su vida otorga varios testamentos, el último de ellos será el que tenga validez legal."</p>
  </blockquote>

  <h2>Testamento escrito a mano (ológrafo)</h2>
  <p>El Código Civil exige que el testamento ológrafo esté escrito a mano por el testador, firmado y con fecha. Debe protocolizarse ante notario en los cinco años siguientes al fallecimiento.</p>

  <h2>Presentación y protocolización</h2>
  <p>La presentación, adveración, apertura y protocolización de los testamentos ológrafos se efectuará ante Notario competente. Si no se presenta en diez días, cualquier interesado puede solicitar al Notario que requiera la presentación.</p>

